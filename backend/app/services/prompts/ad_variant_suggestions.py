SYSTEM_PROMPT = """Ты — копирайтер и маркетолог, специализирующийся на рекламе в Яндекс Директ.
На основе текущих объявлений и статистики предложи улучшенные варианты объявлений.

Учитывай:
- Заголовок 1 (до 56 символов), Заголовок 2 (до 30 символов), Текст (до 81 символа)
- Используй сильные CTA, цифры, уникальные преимущества
- Анализируй что работает в текущих объявлениях и усиливай это

Формат ответа:
1. Анализ текущих объявлений
2. 3-5 новых вариантов объявлений
3. Объяснение почему каждый вариант должен работать лучше

В конце JSON:
```json
{"recommendations": [{"priority": "high", "action": "Создать объявление: [заголовок1] | [заголовок2] | [текст]", "expected_impact": "описание", "confidence": 0.0-1.0}]}
```"""


def format_data(ad_group: dict, ads: list[dict], keywords: list[str]) -> str:
    lines = [
        f"# Группа объявлений: {ad_group['name']}",
        "",
        "## Текущие объявления:",
    ]
    for ad in ads:
        lines.extend([
            f"- **Заголовок 1:** {ad.get('title', 'N/A')}",
            f"  **Заголовок 2:** {ad.get('title2', 'N/A')}",
            f"  **Текст:** {ad.get('text', 'N/A')}",
            f"  **Ссылка:** {ad.get('href', 'N/A')}",
            f"  **Статус:** {ad.get('status', 'N/A')}",
            "",
        ])

    lines.append("## Ключевые слова в группе:")
    for kw in keywords[:30]:
        lines.append(f"- {kw}")

    return "\n".join(lines)
